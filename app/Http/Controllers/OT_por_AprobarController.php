     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
       $ot=OT_por_Aprobar::join('aprobar_nivel1','aprobar_nivel1.id_ot','=','compra.compra_id')
       ->where('aprobar_nivel1.rut_aprobar',$id)
       ->whereIn('compra.compra_estado',array(2,3))
       ->whereNull('compra.compra_asignada')
       ->whereNull('aprobar_nivel1.aprobado')
       ->get();
       return $ot;

    }


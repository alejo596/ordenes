     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
         $p=Refrendacionporcc::join('refrendacion_envio','refrendacion.id','=','refrendacion_envio.id_refrendacion')
         ->where('refrendacion_envio.year',$request->year)
         ->where('refrendacion_envio.cc','=',$request->cc)->where('refrendacion_envio.estado','=',1)
         ->where('refrendacion_envio.sigfe','=',$request->sigfe)
         ->sum('refrendacion_envio.monto');

         return $p;
       
    }



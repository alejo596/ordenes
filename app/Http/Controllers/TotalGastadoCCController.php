    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $year=date("Y");
        $gasto=TotalGastadoCC::where('gasto_pre_id_cr',$id)
        ->where('gasto_pre_sub_sigfe_familia','=',22)
        ->where('gasto_pre_amio','=',$year)
        ->where('gasto_pre_estado','=',2)
        ->whereNull('gasto_pre_convenio')
        ->sum('gasto_pre_monto');
        return $gasto;
    }


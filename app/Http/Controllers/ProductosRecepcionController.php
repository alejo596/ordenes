    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $p=new ProductosRecepcion();
        $p->id_recepcion=$request->recepcion;   
        $p->cod_producto=$request->codigo;        
        $p->cantidad_prod=$request->cantidad;
        $p->valor_unitario=$request->valor;
        $p->id_unidad_medida=$request->medida;     
        $p->id_bodega=$request->idbodega;
        $p->cantidad_quedan=$request->quedan;
        $p->id_producto=$request->productoid;
        $p->save();

        return $p->id;
    }

